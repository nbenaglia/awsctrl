// GetNotificationARNs is an autogenerated deepcopy function, will return notifications for stack
func (in *AccessPoint) GetNotificationARNs() []string {
	notifcations := []string{}
	for _, notifarn := range in.Spec.NotificationARNs {
		notifcations = append(notifcations, *notifarn)
	}
	return notifcations
}

// GetTemplate will return the JSON version of the CFN to use.
func (in *AccessPoint) GetTemplate(client dynamic.Interface) (string, error) {
	if client == nil {
		return "", fmt.Errorf("k8s client not loaded for template")
	}
	template := cloudformation.NewTemplate()

	template.Description = "AWS Controller - s3.AccessPoint (ac-{TODO})"

	template.Outputs = map[string]interface{}{
		"ResourceRef": map[string]interface{}{
			"Value": cloudformation.Ref("AccessPoint"),
			"Export": map[string]interface{}{
				"Name": in.Name + "Ref",
			},
		},
	}

	s3AccessPoint := &s3.AccessPoint{}

	if in.Spec.Name != "" {
		s3AccessPoint.Name = in.Spec.Name
	}

	if in.Spec.NetworkOrigin != "" {
		s3AccessPoint.NetworkOrigin = in.Spec.NetworkOrigin
	}

	if in.Spec.Policy != "" {
		s3AccessPointJSON := make(map[string]interface{})
		err := json.Unmarshal([]byte(in.Spec.Policy), &s3AccessPointJSON)
		if err != nil {
			return "", err
		}
		s3AccessPoint.Policy = s3AccessPointJSON
	}

	if in.Spec.PolicyStatus != "" {
		s3AccessPointJSON := make(map[string]interface{})
		err := json.Unmarshal([]byte(in.Spec.PolicyStatus), &s3AccessPointJSON)
		if err != nil {
			return "", err
		}
		s3AccessPoint.PolicyStatus = s3AccessPointJSON
	}

	if !reflect.DeepEqual(in.Spec.PublicAccessBlockConfiguration, AccessPoint_PublicAccessBlockConfiguration{}) {
		s3AccessPointPublicAccessBlockConfiguration := s3.AccessPoint_PublicAccessBlockConfiguration{}

		if in.Spec.PublicAccessBlockConfiguration.BlockPublicAcls || !in.Spec.PublicAccessBlockConfiguration.BlockPublicAcls {
			s3AccessPointPublicAccessBlockConfiguration.BlockPublicAcls = in.Spec.PublicAccessBlockConfiguration.BlockPublicAcls
		}

		if in.Spec.PublicAccessBlockConfiguration.BlockPublicPolicy || !in.Spec.PublicAccessBlockConfiguration.BlockPublicPolicy {
			s3AccessPointPublicAccessBlockConfiguration.BlockPublicPolicy = in.Spec.PublicAccessBlockConfiguration.BlockPublicPolicy
		}

		if in.Spec.PublicAccessBlockConfiguration.IgnorePublicAcls || !in.Spec.PublicAccessBlockConfiguration.IgnorePublicAcls {
			s3AccessPointPublicAccessBlockConfiguration.IgnorePublicAcls = in.Spec.PublicAccessBlockConfiguration.IgnorePublicAcls
		}

		if in.Spec.PublicAccessBlockConfiguration.RestrictPublicBuckets || !in.Spec.PublicAccessBlockConfiguration.RestrictPublicBuckets {
			s3AccessPointPublicAccessBlockConfiguration.RestrictPublicBuckets = in.Spec.PublicAccessBlockConfiguration.RestrictPublicBuckets
		}

		s3AccessPoint.PublicAccessBlockConfiguration = &s3AccessPointPublicAccessBlockConfiguration
	}

	if !reflect.DeepEqual(in.Spec.VpcConfiguration, AccessPoint_VpcConfiguration{}) {
		s3AccessPointVpcConfiguration := s3.AccessPoint_VpcConfiguration{}

		// TODO(christopherhein) move these to a defaulter
		s3AccessPointVpcConfigurationVpcRefItem := in.Spec.VpcConfiguration.VpcRef.DeepCopy()

		if s3AccessPointVpcConfigurationVpcRefItem.ObjectRef.Namespace == "" {
			s3AccessPointVpcConfigurationVpcRefItem.ObjectRef.Namespace = in.Namespace
		}

		in.Spec.VpcConfiguration.VpcRef = *s3AccessPointVpcConfigurationVpcRefItem
		vpcId, err := in.Spec.VpcConfiguration.VpcRef.String(client)
		if err != nil {
			return "", err
		}

		if vpcId != "" {
			s3AccessPointVpcConfiguration.VpcId = vpcId
		}

		s3AccessPoint.VpcConfiguration = &s3AccessPointVpcConfiguration
	}

	if in.Spec.Bucket != "" {
		s3AccessPoint.Bucket = in.Spec.Bucket
	}

	if in.Spec.CreationDate != "" {
		s3AccessPoint.CreationDate = in.Spec.CreationDate
	}

	template.Resources = map[string]cloudformation.Resource{
		"AccessPoint": s3AccessPoint,
	}

	// json, err := template.JSONWithOptions(&intrinsics.ProcessorOptions{NoEvaluateConditions: true})
	json, err := template.JSON()
	if err != nil {
		return "", err
	}

	return string(json), nil
}

// GetStackID will return stackID
func (in *AccessPoint) GetStackID() string {
	return in.Status.StackID
}

// GenerateStackName will generate a StackName
func (in *AccessPoint) GenerateStackName() string {
	return strings.Join([]string{"s3", "accesspoint", in.GetName(), in.GetNamespace()}, "-")
}

// GetStackName will return stackName
func (in *AccessPoint) GetStackName() string {
	return in.Spec.StackName
}

// GetTemplateVersionLabel will return the stack template version
func (in *AccessPoint) GetTemplateVersionLabel() (value string, ok bool) {
	value, ok = in.Labels[controllerutils.StackTemplateVersionLabel]
	return
}

// GetParameters will return CFN Parameters
func (in *AccessPoint) GetParameters() map[string]string {
	params := map[string]string{}
	cfnencoder.MarshalTypes(params, in.Spec, "Parameter")
	return params
}

// GetCloudFormationMeta will return CFN meta object
func (in *AccessPoint) GetCloudFormationMeta() metav1alpha1.CloudFormationMeta {
	return in.Spec.CloudFormationMeta
}

// GetStatus will return the CFN Status
func (in *AccessPoint) GetStatus() metav1alpha1.ConditionStatus {
	return in.Status.Status
}

// SetStackID will put a stackID
func (in *AccessPoint) SetStackID(input string) {
	in.Status.StackID = input
	return
}

// SetStackName will return stackName
func (in *AccessPoint) SetStackName(input string) {
	in.Spec.StackName = input
	return
}

// SetTemplateVersionLabel will set the template version label
func (in *AccessPoint) SetTemplateVersionLabel() {
	if len(in.Labels) == 0 {
		in.Labels = map[string]string{}
	}

	in.Labels[controllerutils.StackTemplateVersionLabel] = controllerutils.ComputeHash(in.Spec)
}

// TemplateVersionChanged will return bool if template has changed
func (in *AccessPoint) TemplateVersionChanged() bool {
	// Ignore bool since it will still record changed
	label, _ := in.GetTemplateVersionLabel()
	return label != controllerutils.ComputeHash(in.Spec)
}

// SetStatus will set status for object
func (in *AccessPoint) SetStatus(status *metav1alpha1.StatusMeta) {
	in.Status.StatusMeta = *status
}
