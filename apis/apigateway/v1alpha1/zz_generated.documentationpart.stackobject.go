// GetNotificationARNs is an autogenerated deepcopy function, will return notifications for stack
func (in *DocumentationPart) GetNotificationARNs() []string {
	notifcations := []string{}
	for _, notifarn := range in.Spec.NotificationARNs {
		notifcations = append(notifcations, *notifarn)
	}
	return notifcations
}

// GetTemplate will return the JSON version of the CFN to use.
func (in *DocumentationPart) GetTemplate(client dynamic.Interface) (string, error) {
	if client == nil {
		return "", fmt.Errorf("k8s client not loaded for template")
	}
	template := cloudformation.NewTemplate()

	template.Description = "AWS Controller - apigateway.DocumentationPart (ac-{TODO})"

	template.Outputs = map[string]interface{}{
		"ResourceRef": map[string]interface{}{
			"Value": cloudformation.Ref("DocumentationPart"),
		},
	}

	apigatewayDocumentationPart := &apigateway.DocumentationPart{}

	if !reflect.DeepEqual(in.Spec.Location, DocumentationPart_Location{}) {
		apigatewayDocumentationPartLocation := apigateway.DocumentationPart_Location{}

		if in.Spec.Location.Name != "" {
			apigatewayDocumentationPartLocation.Name = in.Spec.Location.Name
		}

		if in.Spec.Location.Path != "" {
			apigatewayDocumentationPartLocation.Path = in.Spec.Location.Path
		}

		if in.Spec.Location.StatusCode != "" {
			apigatewayDocumentationPartLocation.StatusCode = in.Spec.Location.StatusCode
		}

		if in.Spec.Location.Type != "" {
			apigatewayDocumentationPartLocation.Type = in.Spec.Location.Type
		}

		if in.Spec.Location.Method != "" {
			apigatewayDocumentationPartLocation.Method = in.Spec.Location.Method
		}

		apigatewayDocumentationPart.Location = &apigatewayDocumentationPartLocation
	}

	if in.Spec.Properties != "" {
		apigatewayDocumentationPart.Properties = in.Spec.Properties
	}

	// TODO(christopherhein) move these to a defaulter
	apigatewayDocumentationPartRestApiItem := in.Spec.RestApi.DeepCopy()

	if apigatewayDocumentationPartRestApiItem.ObjectRef.Kind == "" {
		apigatewayDocumentationPartRestApiItem.ObjectRef.Kind = "Deployment"
	}

	if apigatewayDocumentationPartRestApiItem.ObjectRef.APIVersion == "" {
		apigatewayDocumentationPartRestApiItem.ObjectRef.APIVersion = "apigateway.awsctrl.io/v1alpha1"
	}

	if apigatewayDocumentationPartRestApiItem.ObjectRef.Namespace == "" {
		apigatewayDocumentationPartRestApiItem.ObjectRef.Namespace = in.Namespace
	}

	in.Spec.RestApi = *apigatewayDocumentationPartRestApiItem
	restApiId, err := in.Spec.RestApi.String(client)
	if err != nil {
		return "", err
	}

	if restApiId != "" {
		apigatewayDocumentationPart.RestApiId = restApiId
	}

	template.Resources = map[string]cloudformation.Resource{
		"DocumentationPart": apigatewayDocumentationPart,
	}

	// json, err := template.JSONWithOptions(&intrinsics.ProcessorOptions{NoEvaluateConditions: true})
	json, err := template.JSON()
	if err != nil {
		return "", err
	}

	return string(json), nil
}

// GetStackID will return stackID
func (in *DocumentationPart) GetStackID() string {
	return in.Status.StackID
}

// GenerateStackName will generate a StackName
func (in *DocumentationPart) GenerateStackName() string {
	return strings.Join([]string{"apigateway", "documentationpart", in.GetName(), in.GetNamespace()}, "-")
}

// GetStackName will return stackName
func (in *DocumentationPart) GetStackName() string {
	return in.Spec.StackName
}

// GetTemplateVersionLabel will return the stack template version
func (in *DocumentationPart) GetTemplateVersionLabel() (value string, ok bool) {
	value, ok = in.Labels[controllerutils.StackTemplateVersionLabel]
	return
}

// GetParameters will return CFN Parameters
func (in *DocumentationPart) GetParameters() map[string]string {
	params := map[string]string{}
	cfnencoder.MarshalTypes(params, in.Spec, "Parameter")
	return params
}

// GetCloudFormationMeta will return CFN meta object
func (in *DocumentationPart) GetCloudFormationMeta() metav1alpha1.CloudFormationMeta {
	return in.Spec.CloudFormationMeta
}

// GetStatus will return the CFN Status
func (in *DocumentationPart) GetStatus() metav1alpha1.ConditionStatus {
	return in.Status.Status
}

// SetStackID will put a stackID
func (in *DocumentationPart) SetStackID(input string) {
	in.Status.StackID = input
	return
}

// SetStackName will return stackName
func (in *DocumentationPart) SetStackName(input string) {
	in.Spec.StackName = input
	return
}

// SetTemplateVersionLabel will set the template version label
func (in *DocumentationPart) SetTemplateVersionLabel() {
	if len(in.Labels) == 0 {
		in.Labels = map[string]string{}
	}

	in.Labels[controllerutils.StackTemplateVersionLabel] = controllerutils.ComputeHash(in.Spec)
}

// TemplateVersionChanged will return bool if template has changed
func (in *DocumentationPart) TemplateVersionChanged() bool {
	// Ignore bool since it will still record changed
	label, _ := in.GetTemplateVersionLabel()
	return label != controllerutils.ComputeHash(in.Spec)
}

// SetStatus will set status for object
func (in *DocumentationPart) SetStatus(status *metav1alpha1.StatusMeta) {
	in.Status.StatusMeta = *status
}
